    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Obtener los valores de los campos de entrada
        String nombre = txtName.getText();
        String stockString = txtStock.getText();
        int cantidad = (Integer.parseInt(stockString));
        String precioString = txtPrecio.getText();
        precioString = precioString.replace(",", ".");
        double precio = Double.parseDouble(precioString);
        
        try{
            ProductoCTRL productoCtrl = new ProductoCTRL();
            boolean resultado = productoCtrl.guardarProducto(nombre, cantidad, precio);

            if(resultado){
                JOptionPane.showMessageDialog(this, "Producto guardado correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo guardar el producto", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error de", "ERROR DE EXCEPCION", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

